    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection conn = null;
        Statement stmt = null;
        String sql = null;
        reportType = stockReportComboBox.getSelectedItem().toString();
        try{
            conn = new DatabaseConnection().connectToBilling();
            stmt = conn.createStatement();
            if(reportType == "Product Wise Stock Report"){
                sql = "select productId, productName, (quantity) as Remaining_Quantity "
                        + "from products";
            } else if (reportType == "Supplier Wise Stock Report"){
                sql = "select s.supplierID, s.supplierName, sum(p.quantity) as Quantity_of_products, "
                        + "((p.quantity-p.soldQuantity)*p.productCost) as Total_Value_Products "
                        + "from supplier s, products p "
                        + "where p.supplierID = s.supplierID   group by s.supplierID";
            } 
            ResultSet rs = stmt.executeQuery(sql);
            ResultSetMetaData rsMetaData = rs.getMetaData();
            int count = rsMetaData.getColumnCount();
            String columnName[] = new String[count];
            for(int i = 1;i <= count;i++){
                columnName[i-1] = rsMetaData.getColumnLabel(i);
            }
            DefaultTableModel model = (DefaultTableModel)reportTable.getModel() ;
            model.setColumnIdentifiers(columnName);
            while(rs.next()){
                Object[] row = new Object[count];
                for(int i= 1; i <= count; i++) {
                    row[i-1] = rs.getString(i);
                }
                model.addRow(row);
            }
        }catch (SQLException ex) {
            Logger.getLogger(FrontEnd.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

